    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed
     try {
         String cadena;
        FileReader f= new FileReader("partidos.txt");
          BufferedReader b = new BufferedReader (f);  
          javax.swing.DefaultListModel modelo = new javax.swing.DefaultListModel();

          while((cadena = b.readLine())!=null){
              modelo.addElement(cadena);
              jList1.setModel(modelo);
          }
          JOptionPane.showMessageDialog(rootPane, "Fichero leido","Mensaje",JOptionPane.PLAIN_MESSAGE);
         b.close();
     } catch (Exception  ex) {
         System.out.println("Error al leer fichero");
     }
     
    }//GEN-LAST:event_jToggleButton3ActionPerformed

