    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     String nombre= Jnombre.getText();
     String siglas = Jsiglas.getText();
     String logo = Jlogo.getText();
     PartidoPolitico partido = new PartidoPolitico(nombre,siglas,logo,nombre,siglas,logo);
     try{
            FileInputStream archivo = new FileInputStream("PartidosPoliticos.dat");
            ObjectInputStream obj_archivo = new ObjectInputStream(archivo);
            partidos= (ArrayList<PartidoPolitico>) obj_archivo.readObject();
            obj_archivo.close();
        }catch(Exception Ex){
            System.out.println("Error al leer o ficheor vacio");
        }
     
     partidos.add(partido);
      try{
        FileOutputStream archivo2 = new FileOutputStream("PartidosPoliticos.dat");
        ObjectOutputStream obj_archivo2 = new ObjectOutputStream(archivo2);
        obj_archivo2.writeObject(partidos);
        obj_archivo2.close();
          JOptionPane.showMessageDialog(this, "Partido Creado");
       }catch(Exception ex){
           System.out.println("Error al guardar");
       }
      MenuElecciones obj=new MenuElecciones();
    obj.setVisible(true);
    dispose();     
    }//GEN-LAST:event_jButton1ActionPerformed

